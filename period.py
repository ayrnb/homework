for prime in primes:
    # 遍历序列A中的索引
    for i in range(100):
        # 如果A[i]为1，并且i是prime的倍数，则将i添加到对应周期长度的列表中
        if A[i] == 1 and i % prime == 0:
            if prime not in periodic_values_by_period:
                periodic_values_by_period[prime] = []
            periodic_values_by_period[prime].append(i)

# 打印按不同周期划分的周期性i值
print("Periodic values by period:")
for period, values in periodic_values_by_period.items():
    print("Period:", period)
    print("Values:", values)
